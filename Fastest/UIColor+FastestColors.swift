import UIKit

extension UIColor {
    
    class var numbersGray: UIColor {
        return UIColor(red: 106/255, green: 105/255, blue: 105/255, alpha: 0.5)
    }
    
    class var numbersLightGray: UIColor {
        return UIColor(red: 222/255, green: 217/255, blue: 217/255, alpha: 1)
    }
}
